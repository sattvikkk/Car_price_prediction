import pandas as pd
import numpy as np
import seaborn as sns
import matplotlib.pyplot as plt
from sklearn.model_selection import train_test_split
from sklearn.preprocessing import StandardScaler
from sklearn.model_selection import RandomizedSearchCV
from sklearn.linear_model import LogisticRegression
from sklearn.metrics import accuracy_score
from sklearn.metrics import confusion_matrix
from sklearn.metrics import classification_report
from sklearn.ensemble import RandomForestRegressor
from sklearn.ensemble import ExtraTreesRegressor
import warnings
warnings.filterwarnings('ignore')

df=pd.read_csv('/content/car data (1).csv')
df

print(df.Seller_Type.unique())
print(df.Transmission.unique())
print(df.Owner.unique())
print(df.Fuel_Type.unique())

df.isnull().sum()

df.describe(include='all')

fdf=df[['Year','Selling_Price','Present_Price','Kms_Driven','Fuel_Type','Seller_Type','Transmission','Owner']]
fdf

fdf['Current_Year']=2026

fdf['no_year']=fdf['Current_Year']-fdf['Year']
fdf.head()

fdf.drop(['Year'],axis=1,inplace=True)
fdf.drop(['Current_Year'],axis=1,inplace=True)
df.drop(['Car_Name'],axis=1,inplace=True)
fdf.head()

fdf=pd.get_dummies(fdf,drop_first=True)*1
fdf.head()

fdf.corr()

corrmat=fdf.corr()
top_corr_features=corrmat.index
plt.figure(figsize=(20,20))
g=sns.heatmap(fdf[top_corr_features].corr(),annot=True,cmap='RdYlGn')

scaler=StandardScaler()

fdf['Kms_Driven'] = scaler.fit_transform(fdf[['Kms_Driven']])

X=fdf.iloc[:,1:]
y=fdf.iloc[:,0]

X['Owner'].unique()

model=ExtraTreesRegressor()
model.fit(X,y)

print(model.feature_importances_)

ft_importance=pd.Series(model.feature_importances_,index=X.columns)
ft_importance.nlargest(20).plot(kind='barh')
plt.show()

X_train, X_test, y_train, y_test = train_test_split(X, y, test_size=0.3, random_state=42)

#randomized search CV for defining the param of the trees

n_estimators=[int(x) for x in np.linspace(start=100,stop=1200,num=12)]
max_features=['sqrt', 'log2']
max_depth=[int(x) for x in np.linspace(5,30,num=6)]
min_samples_split=[2,5,10,15,100]
min_samples_leaf=[1,2,5,10]

random_grid={'n_estimators':n_estimators,
             'max_features':max_features,
             'max_depth':max_depth,
             'min_samples_split':min_samples_split,
             'min_samples_leaf':min_samples_leaf}

print(random_grid)

rf=RandomForestRegressor() # Changed to RandomForestRegressor
rf_random=RandomizedSearchCV(estimator=rf,
                             param_distributions=random_grid,scoring='neg_mean_squared_error'
                             ,n_iter=10,cv=5,verbose=2,random_state=42,n_jobs=1)

rf_random.fit(X_train,y_train)

rf_random.best_params_

rf_random.best_score_

pred=rf_random.predict(X_test)

sns.histplot(y_test-pred)

plt.scatter(y_test,pred)

#deployment into a project

import pickle
file=open('random_forest_regression_model.pkl','wb')
pickle.dump(rf_random,file)

file.close()





